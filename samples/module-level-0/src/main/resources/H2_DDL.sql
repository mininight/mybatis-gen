-- Table for test
DROP TABLE IF EXISTS `cms_content`;
CREATE TABLE `cms_content` (
  `id` int NOT NULL COMMENT '全局唯一标识',
  `channel_id` int NOT NULL COMMENT '栏目ID',
  `user_id` int NOT NULL COMMENT '撰写管理员ID',
  `org_id` int DEFAULT NULL COMMENT '组织ID',
  `publish_user_id` int DEFAULT NULL COMMENT '发布管理员',
  `model_id` int NOT NULL COMMENT '模型ID',
  `site_id` int NOT NULL COMMENT '站点ID',
  `title` varchar(150) NOT NULL COMMENT '内容标题',
  `title_is_bold` tinyint(1) NOT NULL COMMENT '内容标题是否加粗',
  `title_color` varchar(20) NOT NULL DEFAULT '' COMMENT '内容标题的颜色',
  `short_title` varchar(150) DEFAULT NULL COMMENT '简短标题',
  `release_time` datetime NOT NULL COMMENT '发布时间',
  `offline_time` datetime DEFAULT NULL COMMENT '下线时间',
  `content_secret_id` int DEFAULT NULL COMMENT '内容密级',
  `status` smallint NOT NULL DEFAULT '0' COMMENT '内容状态(1:草稿;  2-初稿   3:流转中;   4:已审核;   5:已发布;  6:退回;  7:下线  8-归档 9 暂存 10 驳回 )',
  `create_type` smallint DEFAULT NULL COMMENT '创建方式（1:直接创建    2:投稿  3:站群推送   4:站群采集   5:复制  6:链接型引用 7:镜像型引用）8 外部采集',
  `is_edit` tinyint(1) NOT NULL DEFAULT '0' COMMENT '是否编辑（0-否   1-是）',
  `sort_num` int NOT NULL DEFAULT '0' COMMENT '排序值',
  `sort_weight` int NOT NULL DEFAULT '0' COMMENT '排序值权重(排序值相同情况下，权重越大，排序越前)',
  `views` int NOT NULL DEFAULT '0' COMMENT '浏览量',
  `comments` int NOT NULL DEFAULT '0' COMMENT '评论量',
  `ups` int NOT NULL DEFAULT '0' COMMENT '点赞数',
  `downs` int NOT NULL DEFAULT '0' COMMENT '点踩数',
  `downloads` int NOT NULL DEFAULT '0' COMMENT '下载量',
  `view_control` smallint NOT NULL DEFAULT '1' COMMENT '浏览设置（1-允许游客访问   2-登录后访问）',
  `comment_control` smallint NOT NULL DEFAULT '2' COMMENT '评论设置(1允许游客评论 2登录后评论  3不允许评论)',
  `is_top` tinyint(1) NOT NULL DEFAULT '0' COMMENT '是否置顶',
  `top_start_time` datetime DEFAULT NULL COMMENT '置顶开始时间',
  `top_end_time` datetime DEFAULT NULL COMMENT '置顶结束时间',
  `is_release_pc` tinyint(1) NOT NULL DEFAULT '1' COMMENT '是否发布至pc（0-否  1-是）',
  `is_release_wap` tinyint(1) NOT NULL DEFAULT '1' COMMENT '是否发布至wap（0-否  1-是）',
  `is_release_app` tinyint(1) NOT NULL DEFAULT '1' COMMENT '是否发布至app（0-否  1-是）',
  `is_release_miniprogram` tinyint(1) NOT NULL DEFAULT '1' COMMENT '是否发布至小程序（0-否  1-是）',
  `is_recycle` tinyint(1) NOT NULL DEFAULT '0' COMMENT '是否加入回收站（0-否  1-是）',
  `copy_source_content_id` int DEFAULT NULL COMMENT '复制来源内容id',
  `has_static` tinyint(1) NOT NULL DEFAULT '0' COMMENT '是否已生成静态化页面',
  `check_mark` varchar(150) DEFAULT NULL COMMENT '内容审核标识',
  `create_time` datetime NOT NULL COMMENT '创建时间',
  `update_time` datetime DEFAULT NULL COMMENT '修改时间',
  `create_user` varchar(50) NOT NULL COMMENT '创建者',
  `update_user` varchar(50) DEFAULT NULL COMMENT '修改者',
  `deleted_flag` tinyint(1) NOT NULL COMMENT '删除标识（0为正常1为删除）',
  `model_field_set` varchar(2000) DEFAULT NULL COMMENT '内容快照：记录修改当前内容的模型的所有字段',
  `content_pos` smallint DEFAULT '1' COMMENT '内容列表位置(1 内容列表 2智能审核 3回收站 4已归档)',
  `ori_content_id` int DEFAULT NULL COMMENT '引用内容的原内容ID',
  `people_views` int DEFAULT '0' COMMENT '浏览人数',
  PRIMARY KEY (`id`)
) DEFAULT CHARSET=utf8 COMMENT='内容主体表';